package com.kodilla.rps;

public class User {
    String userName;
    int rounds;

    public String getUserName() {
        return userName;
    }

    public int getRounds() {
        return rounds;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public void setRounds(int rounds) {
        this.rounds = rounds;
    }
}

